import gradio as gr
from transformers import pipeline
import torch

# 🛑 التعديل البرمجي لربط الموديل السحابي مباشرة 🛑

HF_MODEL_REPO = "Alhareth7790/Sabq-News-Classifier"

print("⏳ جاري تحميل النموذج مباشرة من Hugging Face Hub (v2)...")
try:
    classifier = pipeline(
        "text-classification",
        model=HF_MODEL_REPO,
        tokenizer=HF_MODEL_REPO,
        framework="pt"
    )
    print("✅ النموذج جاهز وتم تحميله بنجاح!")
except Exception as e:
    print(f"❌ فشل تحميل النموذج. تأكد من اسم المستودع. الخطأ: {e}")
    classifier = None

def classify_text(text):
    if not text or not text.strip():
        return "الرجاء إدخال نص صحيح"
    
    if classifier is None:
        return "عذراً، الموديل غير متوفر حالياً. تأكد من إعدادات الرابط."
        
    # 1. اطلب النتيجة الأعلى (top_k=1) فقط
    results = classifier(text, truncation=True, max_length=256, top_k=1)
        
    # 2. استخرج النتيجة (ستكون عنصراً واحداً في قائمة)
    top_result = results[0]
    label = top_result['label']
    score = top_result['score']
        
    # 3. أعد النتيجة كنص منسق (سطرين)
    return f"التصنيف: {label}\nدرجة الثقة: {score:.2%}"

